package cn.liyw.domin;

public class CallbackActivityConf {
    private Integer amountLimit;
    private Integer userAmountLimit;
    private Integer consumeCuryId;
    private Integer consumeRuleId;
    private Boolean consumeRuleDegrade;
    private Integer activityId;
    private Integer areaId;
    private Integer appId;

    public Integer getAmountLimit() {
        return amountLimit;
    }

    public void setAmountLimit(Integer amountLimit) {
        this.amountLimit = amountLimit;
    }

    public Integer getUserAmountLimit() {
        return userAmountLimit;
    }

    public void setUserAmountLimit(Integer userAmountLimit) {
        this.userAmountLimit = userAmountLimit;
    }

    public Integer getConsumeCuryId() {
        return consumeCuryId;
    }

    public void setConsumeCuryId(Integer consumeCuryId) {
        this.consumeCuryId = consumeCuryId;
    }

    public Integer getConsumeRuleId() {
        return consumeRuleId;
    }

    public void setConsumeRuleId(Integer consumeRuleId) {
        this.consumeRuleId = consumeRuleId;
    }

    public Boolean getConsumeRuleDegrade() {
        return consumeRuleDegrade;
    }

    public void setConsumeRuleDegrade(Boolean consumeRuleDegrade) {
        this.consumeRuleDegrade = consumeRuleDegrade;
    }

    public Integer getActivityId() {
        return activityId;
    }

    public void setActivityId(Integer activityId) {
        this.activityId = activityId;
    }

    public Integer getAreaId() {
        return areaId;
    }

    public void setAreaId(Integer areaId) {
        this.areaId = areaId;
    }

    public Integer getAppId() {
        return appId;
    }

    public void setAppId(Integer appId) {
        this.appId = appId;
    }
}
